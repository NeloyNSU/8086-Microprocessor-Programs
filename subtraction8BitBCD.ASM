;Subtract 8 bit BCD numbers

ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	NUM1 DB 09H
	NUM2 DB 06H
	RESULT DB 00H,13,10,'$'
	MSG DB 13,10,"ANSWER OF 8 BIT SUBTRACTION IS: ",'$'
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AL,NUM1
	SUB AL,NUM2
	ADD AL,30H
	MOV RESULT,AL

	LEA DX,MSG
	MOV AH,09H
	INT 21H
	
	LEA DX,RESULT
	INT 21H
	
	MOV AH,4CH
	INT 21h
	CODE ENDS
END START