;To find the Factorial of a Number using Recursive procedure

INCLUDE IO.H
ASSUME CS:CODE,DS:DATA,SS:STACK

STACK SEGMENT
	DW 40 DUP(0)
	TOP LABEL WORD
STACK ENDS

DATA SEGMENT
	ALERT DB 13,10,"	FACTORIAL : ",0
	NUM1 DB 06H
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	MOV AX,STACK
	MOV SS,AX
	LEA SP,TOP
	
	MOV AX,0001H
	MOV DX,0000H
	MOV CX,0000H
	MOV CL,NUM1
	
	CALL FACT
	
	OUTPUT ALERT
	ITOA ANS,AX
	OUTPUT ANS
	JMP QUIT	
	
FACT PROC
	CMP CL,00H
	JE GO
	PUSH CX
	DEC CX
	CALL FACT
	POP CX
	MUL CL
GO:
	RET
FACT ENDP
	
QUIT:
	MOV AH,4CH
	INT 21H
CODE ENDS
END START