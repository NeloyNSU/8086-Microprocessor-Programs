;Multiply two 8 bit numbers using add shift method

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	MSG DB 10,"  ADD SHIFT MULTIPLICATION ANSWER : ",0
	NUM1 DB 09H
	NUM2 DB 0CH
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AX,0000H
	MOV BX,0000H
	MOV DX,0000H
	MOV CX,0007H

	MOV AL,NUM1
	MOV BL,NUM2
	SHR BL,1
	JNC POOL
	MOV DX,AX
POOL:
	SHL AX,1
	SHR BL,1
	JNC DOWN
	ADD DX,AX
DOWN:
	LOOP POOL
	
	OUTPUT MSG
	ITOA ANS,DX
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START