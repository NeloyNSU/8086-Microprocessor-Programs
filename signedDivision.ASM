;Divide 16 bit signed numbers by a 16 bit signed numbers
ASSUME CS:CODE,DS:DATA
INCLUDE IO.H
DATA SEGMENT
	ALERT DB 13,10,"	RESULT OF SIGNED DIVISION : ",0
	NUM1 DW 0258H
	NUM2 DW -0014H
	RESULT DW 00H
	ANS DW 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AX,NUM1
	IDIV NUM2
	MOV RESULT,AX
	
	OUTPUT ALERT
	ITOA ANS,RESULT
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START

