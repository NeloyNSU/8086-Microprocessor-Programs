;Program to find the number of negative numbers in the array

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	ARR DW -2DH,0004H,-70H,0003H,-0002H
	LEN EQU ($-ARR)
	ANS DB 6 DUP(0),0
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV SI,0
	MOV AX,0000H
	MOV CX,0000H
BACK:
	MOV AX,ARR[SI]
	ROL AX,1
	JNC GO
	INC CX
GO:
	ADD SI,2
	CMP SI,LEN
	JB BACK
	
	ITOA ANS,CX
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START