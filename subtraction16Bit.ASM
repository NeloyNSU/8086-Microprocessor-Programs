;16Bit SUBTRACTION
ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	NUM1 DW 4050H
	NUM2 DW 1050H
	RESULT DW 0000H,13,10,'$'
	MSG DB 13,10,"ANSWER OF 16 BIT SUBTRACTION IS: ",'$'
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AX,NUM1
	MOV BX,NUM2
	SUB AX,BX
	MOV RESULT,AX
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START


