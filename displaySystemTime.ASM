; Program to display current system time

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	MSG DB 10,"	TIME IS : ",0
	HR DB " HOURS",0
	MIN DB " MINUTES",0
	SEC DB " SECONDS",10,0
	TIME DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AH,2CH
	INT 21H
	
	OUTPUT MSG
	MOV AX,0000H
	
	MOV AL,CH
	ITOA TIME,AX
	OUTPUT TIME
	OUTPUT HR
	
	MOV AL,CL
	ITOA TIME,AX
	OUTPUT TIME
	OUTPUT MIN
	
	MOV AL,DH
	ITOA TIME,AX
	OUTPUT TIME
	OUTPUT SEC
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START