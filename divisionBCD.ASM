;Divide two BCD numbers

INCLUDE IO.H
ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	ALERT DB 13,10,"	RESULT OF DIVISION : ",0
	NUM1 DW 0603H
	NUM2 DB 07H
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AX,0000H
	MOV DX,0000H
	MOV AX,NUM1
	MOV BL,NUM2
	
	AAD
	DIV BL
	
	MOV AH,00H
	OUTPUT ALERT
	ITOA ANS,AX
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START