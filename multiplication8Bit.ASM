;8bit Multiplication 
ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	NUM1 DB 07H
	NUM2 DB 09H
	RESULT DW 0000H,13,10,'$'
	MSG DB 13,10,"  ANSWER OF 8 BIT MULTIPLICATION IS: ",'$'
DATA ENDS	

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AH,00H
	MOV AL,NUM1
	MUL NUM2
	AAM
	OR AX,3030H
	XCHG AL,AH
	MOV RESULT,AX
	
	LEA DX,MSG
	MOV AH,09H
	INT 21H
	
	LEA DX,RESULT
	INT 21H
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START