;USER INPUT MULTIPLICATION OF SINGLE DIGIT NUMBERS ONLY
INCLUDE IO.H
ASSUME CS:CODE,DS:DATA
DATA SEGMENT
NUM1 DB ?
NUM2 DB ?
RESULT DB ?,"$"
MSG1 DB 13,10,"Enter First Number: $'"
MSG2 DB 13,10,"Enter Second Number: $'"
MSG3 DB 13,10,"The Result is: $'"
DATA ENDS

CODE SEGMENT
Start:
MOV AX,DATA
MOV DS,AX

LEA DX,MSG1
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
SUB AL,30H
MOV NUM1,AL

LEA DX,MSG2
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
SUB AL,30H
MOV NUM2,AL

MUL NUM1
ADD AL,30H
MOV RESULT,AL
LEA DX,MSG3
MOV AH,09H
INT 21H
LEA DX,RESULT
MOV AH,09H
INT 21H

MOV AH,4CH
INT 21H
CODE ENDS
END START
