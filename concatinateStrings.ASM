;To concatenate Two Strings using Procedure

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	PROMPT1 DB 13,10,"	Enter the First string : ",0	
	PROMPT2 DB 13,10,"	Enter the Second string : ",0	
	CONCAT DB 13,10,"	Concatinated String : ",0	
	STR1 DB 20 DUP(0)
	STR2 DB 20 DUP(0)
	RESULT DB 40 DUP(0)
	LEN1 DW 0000H
	LEN2 DW 0000H
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV CX,0000H
	OUTPUT PROMPT1
	INPUTS STR1,20 
	MOV LEN1,CX
	
	MOV CX,0000H
	OUTPUT PROMPT2
	INPUTS STR2,20 
	MOV LEN2,CX
	
	ADD CX,LEN1
	MOV SI,0
	MOV DI,0
	CALL CATIN
	OUTPUT CONCAT
	OUTPUT RESULT
	JMP QUIT
CATIN PROC
LP1:
	MOV AL,STR1[SI]
	MOV RESULT[DI],AL
	INC SI
	INC DI
	CMP SI,LEN1
	JB LP1
	MOV SI,0
LP2:	
	MOV AL,STR2[SI]
	MOV RESULT[DI],AL
	INC SI
	INC DI
	CMP SI,LEN2
	JB LP2
	RET
CATIN ENDP
	
QUIT:
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
