;PROGRAM TO REVERSE A STRING
ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	STRN DB 30 DUP(0)
	RSTRN DB 30 DUP(0)
	LEN DW 0000H,13,10,0
	ALEN DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS, AX
	MOV ES,AX
	MOV CX,0000H
	
	INPUTS STRN,30
	
	MOV LEN,CX
	
	MOV SI,0
	MOV DI,0
	
	ADD SI,LEN
	DEC SI
	
POOL:
	MOV AL,STRN[SI]
	MOV RSTRN[DI],AL
	INC DI
	DEC SI
	LOOP POOL
	
	OUTPUT RSTRN
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START