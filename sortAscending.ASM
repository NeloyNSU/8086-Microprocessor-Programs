;Program to sort the numbers in ascending order
INCLUDE IO.H
ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	ARR DB 05H,04H,07H,03H,02H
	LEN EQU $-ARR
	OP DB 6 DUP(?)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AX,0000H
	MOV DX,LEN
	MOV DI,0
BACK:
	MOV SI,DI
	INC SI
	MOV AL,ARR[DI]
POOL:
	MOV BL,ARR[SI]
	CMP AL,BL
	JC GO
	XCHG AL,ARR[SI]
GO:
	INC SI
	CMP SI,LEN
	JB POOL
	
	MOV ARR[DI],AL
	INC DI
	DEC SI
	CMP DI,SI
	JB BACK
	
	MOV SI,0
PRINT:
	MOV AL,ARR[SI]
	ITOA OP,AX
	OUTPUT OP
	INC SI
	CMP SI,LEN
	JB PRINT
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START