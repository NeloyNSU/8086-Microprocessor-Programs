;To find the Length of the given string
ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	STRN DB 10 DUP(0)
	LEN DW 0000H
	ANS DB 6 DUP(0),13,10,0
	PROMPT DB 13,10,"			Enter the string : ",0
	PLEN DB 13,10,"			Length of string is : ",0
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV CX,0000H
	OUTPUT PROMPT
	INPUTS STRN,10
	MOV LEN,CX
	
	ITOA ANS,LEN
	OUTPUT PLEN
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START
	
	
	
	