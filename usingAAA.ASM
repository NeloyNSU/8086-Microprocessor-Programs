;Program for Simulating AAA Instruction
ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	NUM DB 00H,'$'
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	MOV AH,01H
	INT 21H
	MOV BL,AL
	MOV AH,01H
	INT 21H
	ADD AL,BL
	AAA
	
	ADD AL,30H
	MOV NUM,AL
	
	LEA DX,NUM
	MOV AH,09H
	INT 21H
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START