;Program to sort the numbers in descending order
ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	ARR DB 05H,04H,07H,03H,02H
	LEN EQU $-ARR
	OP DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV DX,LEN
	MOV AX,0000H
	MOV DI,0
	
BACK:
	MOV SI,DI
	INC SI
	MOV AL,ARR[DI]
POOL:
	CMP AL,ARR[SI]
	JNC GO
	XCHG AL,ARR[SI]
GO:
	INC SI
	CMP SI,LEN
	JB POOL
	
	MOV ARR[DI],AL
	INC DI
	DEC SI
	CMP DI,SI
	JB BACK
	
	MOV SI,0
PRINT:
	MOV AL,ARR[SI]
	ITOA OP,AX
	OUTPUT OP
	INC SI
	CMP SI,LEN
	JB PRINT
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START