; To Check If Entered Username Is Valid Or Not
ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	PROMPT DB 13,10,"		Username : ",0
	USR DB "POK"
	LEN EQU $-USR
	ACCEPT DB 13,10,"		User Name is Valid",13,10,0
	REJECT DB 13,10,"		User Name is Invalid",13,10,0
	STRN DB 10 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	MOV ES,AX
	
	MOV CX,0000H
	
	OUTPUT PROMPT
	INPUTS STRN,10
	CMP CX,LEN
	JNE IV
	
	LEA SI,USR
	LEA DI,STRN
	CLD

	REPE CMPSB
	JNZ IV
	
	OUTPUT ACCEPT
	JMP QUIT
	
IV:
	OUTPUT REJECT
QUIT:
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START