;Program to find GCD AND LCM of two numbers

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	GCD DB 10,"		GCD : ",0
	LCM DB 10,"		LCM : ",0
	NUM1 DB 02H
	NUM2 DB 04H
	TEMP DB 00H
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AX,0000H
	MOV BX,0000H
	
	MOV AL,NUM1
	MOV BL,NUM2
AGAIN:
	MOV AH,00H
	CMP BL,00H			; C CODE
	JE DONE				;while(b!=0)
	MOV TEMP,BL			;{
	DIV BL				;	t=b
	MOV BL,AH			;	b=a%b
	MOV AL,TEMP			;	a=t		
	JMP AGAIN			;}

DONE:
	OUTPUT GCD
	ITOA ANS,AX 
	OUTPUT ANS
	
	OUTPUT LCM
	MOV DL,AL
	MOV AL,NUM1
	MOV BL,NUM2
	MUL BL
	DIV DL
	ITOA ANS,AX
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START