;Sum and Average using an array
INCLUDE IO.H
ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	MSG1 DB 13,10,"			SUM 	: ",0
	MSG2 DB 13,10,"			AVERAGE : ",0
	ARR DB 01H,02H,03H,04H,05H,06H,07H,08H,09H,0AH
	LEN DB $-ARR
	ANS DB 6 DUP(0),13,10,0
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV CL,LEN
	MOV SI,0
	MOV AX,0000H
POOL:
	ADD AL,ARR[SI]
	INC SI
	LOOP POOL
	
	OUTPUT MSG1
	ITOA ANS,AX
	OUTPUT ANS
	
	DIV LEN
	MOV AH,00H
	OUTPUT MSG2
	ITOA ANS,AX
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START