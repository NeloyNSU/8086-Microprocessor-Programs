;To find Fibonacci series of N given terms
ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	TMP DW 0000H
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV CX,0000H
	MOV AH,01H
	INT 21H
	AND AL,0FH
	SUB AL,02H
	MOV CL,AL
	
	MOV AX,0000H
	MOV BX,0001H
	
	ITOA ANS,AX
	OUTPUT ANS
	
	ITOA ANS,BX
	OUTPUT ANS
	
POOL:
	ADD AX,BX
	MOV TMP,AX
	MOV AX,BX
	MOV BX,TMP
	
	ITOA ANS,TMP
	OUTPUT ANS
	
	LOOP POOL
	
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START
	