;Program to find maximum number in the array

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	ARR DB 05H,04H,07H,03H,02H
	LEN EQU $-ARR
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV SI,0
	MOV AX,0000H
	MOV AL,ARR[SI]
	INC SI
BACK:
	CMP AL,ARR[SI]
	JA GO
	MOV AL,ARR[SI]
GO:
	INC SI
	CMP SI,LEN
	JB BACK
	
	ITOA ANS,AX
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
