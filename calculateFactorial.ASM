;To find the factorial of a number

INCLUDE IO.H
ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	ALERT DB 13,10,"	FACTORIAL : ",0
	NUM1 DB 05H
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV AX,0001H
	MOV DX,0000H
	MOV CX,0000H
	MOV CL,NUM1
	
POOL:
	MUL CL
	LOOP POOL
	
	OUTPUT ALERT
	ITOA ANS,AX
	OUTPUT ANS
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START