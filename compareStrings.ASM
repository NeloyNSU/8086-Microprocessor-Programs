;Program to compare given strings
ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	STR1 DB 10 DUP(?)
	STR2 DB 10 DUP(?)
	LEN DW 0000H
	PROMT1 DB 13,10,"			ENTER STRING 1 : ",0
	PROMT2 DB 13,10,"			ENTER STRING 2 : ",0
	ACCEPT DB 13,10,"			STRINGS ARE IDENTICAL",13,10,0
	REJECT DB 13,10,"			STRINGS ARE NOT IDENTICAL",13,10,0
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	MOV ES,AX
	
	MOV CX,0000H
	
	OUTPUT PROMT1
	INPUTS STR1,10
	MOV LEN,CX
	
	OUTPUT PROMT2
	INPUTS STR2,10

	CMP CX,LEN
	JNE RJ

	LEA SI,STR1
	LEA DI,STR2
	CLD
	REPE CMPSB
	JNZ RJ
	
	OUTPUT ACCEPT
	JMP QUIT
	
RJ:
	OUTPUT REJECT
	
QUIT:
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START