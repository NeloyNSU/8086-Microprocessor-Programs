;Program For Conversion of BCD to Hex

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	PROMPT DB 10,"		BCD : ",0
	MSG1 DB 10,"		BINARY : ",0
	MSG2 DB 10,"		HEXADECIMAL : ",0
	NXT DB 10," ",0
	O DB "1",0
	Z DB "0",0
	ATAB DB 30H,31H,32H,33H,34H,35H,36H,37H,38H,39H,41H,42H,43H,44H,45H,46H
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	OUTPUT PROMPT
	INPUTS ANS,4
	ATOI ANS
	
	OUTPUT MSG1
	MOV CX,0004H
	MOV DL,AL
	SHL DL,CL

POOL:
	SHL DL,1
	JC GO
	OUTPUT Z
	JMP DOWN
GO:
	OUTPUT O
DOWN:
	LOOP POOL
	
	OUTPUT NXT
	
	OUTPUT MSG2
	LEA BX,ATAB
	XLATB
	
	
	MOV ANS,0
	MOV WORD PTR ANS,AX
	OUTPUT ANS
	OUTPUT NXT
	
	MOV AH,4CH
	INT 21H
CODE ENDS
END START