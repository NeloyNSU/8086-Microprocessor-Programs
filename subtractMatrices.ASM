;Program to subtract two 3 X 3 matrices

INCLUDE IO.H
ASSUME CS:CODE,DS:DATA

DATA SEGMENT
	MSG1 DB 13,10,"  MATRIX 1: ",13,10,0
	MSG2 DB 13,10,"  MATRIX 2: ",13,10,0
	MSG3 DB 13,10,"  DIFFERENCE : ",13,10,0
	NXT DB " ",13,10,0
	MAT1 DW 0020H,0230H,0650H,0025H,4020H,0020H,1020H,0450H,0A20H
	MAT2 DW 0340H,0330H,0620H,0720H,0010H,0530H,0220H,0254H,0A2AH
	DEG DW 0003H
	ANS DB 6 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	MOV ES,AX
	
	OUTPUT MSG1
	MOV CX,DEG
	LEA SI,MAT1
	CALL PRINT
	
	OUTPUT MSG2
	MOV CX,DEG
	LEA SI,MAT2
	CALL PRINT
	
	LEA SI,MAT1
	LEA DI,MAT2
	MOV CX,0009H
AD:
	MOV AX,[SI]
	MOV BX,[DI]
	SUB AX,BX
	MOV [SI],AX
	ADD SI,2
	ADD DI,2
	INC BP
	LOOP AD
	
	OUTPUT MSG3
	MOV CX,DEG
	LEA SI,MAT1
	CALL PRINT
	
	JMP QUIT
PRINT PROC
POOL:
	MOV DX,DEG
PL:
	ITOA ANS,[SI]
	OUTPUT ANS
	ADD SI,2
	DEC DX
	JNZ PL
	OUTPUT NXT
	LOOP POOL
	RET
PRINT ENDP
	
QUIT:
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START