;Program for checking the parity of number is Odd or even

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	PROMPT DB 13,10,"			ENTER A NUMBER : ",0
	EVP DB 13,10,"			EVEN PARITY",13,10,0
	ODP DB 13,10,"			ODD PARITY",13,10,0
DATA ENDS
	
CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	OUTPUT PROMPT
	MOV AH,01H
	INT 21H
	
	AND AL,0FH
	JNP NP
	
	OUTPUT EVP
	JMP QUIT
NP:
	OUTPUT ODP
QUIT:
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START