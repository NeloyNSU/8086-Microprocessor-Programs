;	CASE COVERSION (ALPHABETS UPPER CASE AND LOWER CASE)
;	A = 65 AND a = 97
;	Z = 90 AND Z = 122
;
;	DIFFERENCE 
;	90 - 65 = 32

ASSUME CS:CODE,DS:DATA
INCLUDE IO.H

DATA SEGMENT
	PROMPT DB 13,10,"	Enter the string : ",0
	CC DB 13,10,"	Converted string : ",0
	IV DB 13,10,"	Invalid Input",0
	LEN DW 0000H
	STRN DB 10 DUP(0)
DATA ENDS

CODE SEGMENT
START:
	MOV AX,DATA
	MOV DS,AX
	
	MOV CX,0000H
	OUTPUT PROMPT
	INPUTS STRN,10
	MOV LEN,CX
	
	MOV SI,0
BACK:
	CMP STRN[SI],5BH
	JB ULC
	CMP STRN[SI],60H
	JA LUC
	
	OUTPUT IV
	JMP QUIT
	
ULC:
	ADD STRN[SI],20H
	JMP GO
LUC:
	SUB STRN[SI],20H
GO:
	INC SI
	DEC CX
	JNZ BACK 
	
	OUTPUT CC
	OUTPUT STRN
	
QUIT:
	MOV AH,4CH
	INT 21H
	CODE ENDS
END START

